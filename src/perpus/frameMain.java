    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tglpjm = dateFormat.format(pinjam.getDate());
        String tglkbl = dateFormat.format(kembali.getDate());
        String Status = "";
            if(blmkbl.isSelected()){
                Status = "Belum Kembali";
            }else{
                Status = "Kembali";
            }
        String jenisbuku = jenis.getSelectedItem().toString();
            if("".equals(no.getText()) || 
                "".equals(nama.getText()) || 
                "".equals(alamat.getText()) || 
                "".equals(judul.getText()) ||
                "".equals(lama.getText()) ||
                "".equals(denda.getText())){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
                String SQL = "INSERT INTO pengembalian (NoAnggota,NamaPeminjam,Alamat,JenisBuku,JudulBuku,LamaPinjam,Denda,Status)"
                        +"VALUE('"+no.getText()+"','"+nama.getText()+"',"
                        + "'"+alamat.getText()+"','"+jenisbuku+"',"
                        + "'"+lama.getText()+"','"
                        + "'"+judul.getText()+"','"+denda.getText()+"','"+Status+"')";
                int status = KoneksiDB.execute(SQL);
                if(status==1){
                    JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan","Sukses",JOptionPane.INFORMATION_MESSAGE);
                    try {
                        selectData();
                    } catch (SQLException ex) {
                        Logger.getLogger(frameMain.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan","Sukses",JOptionPane.INFORMATION_MESSAGE);
                }
        }
    }//GEN-LAST:event_saveActionPerformed

