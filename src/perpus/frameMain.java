    private void selectData() throws SQLException {
        String kolom[] = {"NoAnggota","Nama","Alamat","JenisBuku","JudulBuku","LamaPinjam","Denda","Status"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM pengembalian";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String NoAnggota = rs.getString(1);
                String Nama = rs.getString(2);
                String Alamat = rs.getString(3);
                String JenisBuku = rs.getString(4);
                String JudulBuku = rs.getString(5);
                String LamaPinjam = rs.getString(6);
                String Denda = rs.getString(7);
                String Status = "";
                if("Belum Kembali".equals(rs.getString(8))){
                    Status = "Belum Kembali";
                }else{
                    Status = "Kembali";
                }
                String data[] = {NoAnggota,Nama,Alamat,JenisBuku,JudulBuku,LamaPinjam,Denda,Status};
                dtm.addRow(data);
            }
        }catch(SQLException ex){
            java.util.logging.Logger.getLogger(frameMain.class.getName()).log(Level.SEVERE,null,ex);
        }
        tabel.setModel(dtm);
    }
